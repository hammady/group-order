<h2>
Welcome to the order system
</h2>
<p>Order system is an automated system for making orders easily. 
Everyone can join an order and choose meals from the shop's menu. 
It integrates with IP Messenger to send messages declaring new orders ready to join.
</p>
<h3>Features of Order System:</h3>
<ol>
	<li>It collects orders easily, without anyone gets busy or involved.</li>
	<li>It has menus for restaurants that can never be lost, it is available even if you made your order manually.</li>
	<li>It keeps history of orders done for paying or for future orders.</li>
	<li>It displays a statistics page to show popular shops and meals.</li>
	<li>It keeps upgrading to meet our exact needs. You can suggest new features or change the way it works.</li>
</ol>
<p>Order system is easy to use. You mainly do only 4 steps to use it:</p>
<ol>
	<li>Log in or sign up. You need to enter correct data. IP will be used to send messages to you.</li>
	<li>Create a new order. You will choose shop to order from, and people to notify about the new order.</li>
	<li>Manage your order by adding meals from the shop menu. You can add notes to meals like "Spicy" or insert a meal if it was not in menu.</li>
	<li>Close the order. You will get a summary page with all information you need to call the shop and make your order.</li>
</ol>
